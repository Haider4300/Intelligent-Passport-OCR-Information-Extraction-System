"""
STEP 1 — Dataset Preparation Script
=====================================
This script:
1. Reads all images and labels from raw_data/
2. Splits them 80% train / 20% val
3. Copies them into the correct dataset/ folder structure for YOLO

Run this FIRST before training.
Usage:
    python prepare_dataset.py
"""

import os
import shutil
import random
from pathlib import Path

# ─── CONFIG ────────────────────────────────────────────────────────────────────
RAW_IMAGES_DIR = "raw_data/images"      # Where your original passport images are
RAW_LABELS_DIR = "raw_data/lables"      # Where your LabelImg .txt files are

DATASET_DIR    = "dataset"
TRAIN_RATIO    = 0.8                    # 80% train, 20% validation
RANDOM_SEED    = 42                     # For reproducibility

# Supported image extensions
IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}
# ───────────────────────────────────────────────────────────────────────────────


def create_folder_structure():
    """Create the required YOLO dataset folder structure."""
    folders = [
        f"{DATASET_DIR}/images/train",
        f"{DATASET_DIR}/images/val",
        f"{DATASET_DIR}/labels/train",
        f"{DATASET_DIR}/labels/val",
    ]
    for folder in folders:
        Path(folder).mkdir(parents=True, exist_ok=True)
    print("✅ Folder structure created.")


def get_image_label_pairs():
    """Find all image files that have a matching label file."""
    raw_images = Path(RAW_IMAGES_DIR)
    raw_labels = Path(RAW_LABELS_DIR)

    pairs = []
    for img_path in sorted(raw_images.iterdir()):
        if img_path.suffix.lower() not in IMAGE_EXTENSIONS:
            continue
        label_path = raw_labels / (img_path.stem + ".txt")
        if label_path.exists():
            pairs.append((img_path, label_path))
        else:
            print(f"⚠️  No label found for: {img_path.name} — skipping.")

    print(f"✅ Found {len(pairs)} image-label pairs.")
    return pairs


def split_and_copy(pairs):
    """Shuffle, split, and copy files into train/val directories."""
    random.seed(RANDOM_SEED)
    random.shuffle(pairs)

    split_idx   = int(len(pairs) * TRAIN_RATIO)
    train_pairs = pairs[:split_idx]
    val_pairs   = pairs[split_idx:]

    print(f"   → Train: {len(train_pairs)} images")
    print(f"   → Val:   {len(val_pairs)} images")

    def copy_pairs(pair_list, split_name):
        for img_path, label_path in pair_list:
            shutil.copy(img_path,   f"{DATASET_DIR}/images/{split_name}/{img_path.name}")
            shutil.copy(label_path, f"{DATASET_DIR}/labels/{split_name}/{label_path.name}")

    copy_pairs(train_pairs, "train")
    copy_pairs(val_pairs,   "val")
    print("✅ Files copied successfully.")


def write_data_yaml():
    """Write the data.yaml config file that YOLO needs."""
    # Use absolute paths to avoid any path confusion
    base = Path(DATASET_DIR).resolve()

    yaml_content = f"""# YOLO Dataset Configuration
# Auto-generated by prepare_dataset.py

path: {base}          # Root dataset directory
train: images/train   # Training images (relative to path)
val:   images/val     # Validation images (relative to path)

# Number of classes
nc: 1

# Class names — add more if you labeled more fields
names:
  0: passport_number
"""
    yaml_path = Path(DATASET_DIR) / "data.yaml"
    yaml_path.write_text(yaml_content)
    print(f"✅ data.yaml written at: {yaml_path.resolve()}")


def main():
    print("\n🚀 Starting dataset preparation...\n")

    create_folder_structure()
    pairs = get_image_label_pairs()

    if not pairs:
        print("❌ No image-label pairs found. Check your RAW_IMAGES_DIR and RAW_LABELS_DIR paths.")
        return

    split_and_copy(pairs)
    write_data_yaml()

    print("\n✅ Dataset preparation complete!")
    print("   Next step → run: python train.py\n")


if __name__ == "__main__":
    main()